			<dt>Modifier un énnoncé</dt>
			<dd>Cette option vous offre la possibilité de modifier un des énnoncés. Elle permet également de consulter la date ainsi que l'auteur de la 
			dernière modification</dd>
			<dt>Importer un niveau</dt>
			<dd>Vous pouvez avec cette option, importer un niveau dans la base de donnée. Toutefois, si le niveau dans lequel vous tentez d'importer
			a déjà été complété, votre importation vous sera refusée</dd>
			<dt>Exporter un niveau</dt>
			<dd>Vous pouvez, grâce à cette option, exporter l'entièreté des query d'un niveau afin d'éventuellement les communiquer à un collègue, 
			garder une trace,...</dd>
		</dl>
