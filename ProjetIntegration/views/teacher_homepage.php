		Bienvenue sur le site d'exercices de query du cours d'IANARCH.<br>
		Sur ce site vous pourrez réaliser différentes actions grâce aux onglets qui vous sont proposés dans le menu:<br>
		<dl>
			<dt>Liste des étudiants</dt>
			<dd>Ici vous pourrez consulter la liste des étudiants ainsi que leur progression dans les exercices. Dans cette page vous pourrez également
